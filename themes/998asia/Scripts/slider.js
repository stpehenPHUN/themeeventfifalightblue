function sortArr(arr) {
    if (!arr || arr.length < 1) return [];
    var newArr = arr
        .map(value => ({ value, sort: Math.random() }))
        .sort((a, b) => a.sort - b.sort)
        .map(({ value }) => value)
    return newArr;
};

function randomNumber(min, max, decimalPlaces) {
    return (Math.random() * (max - min) + min).toFixed(decimalPlaces) * 1;
}

